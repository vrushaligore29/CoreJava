
public class TestLocalVariable {

	public static void main(String[] args) {
		boolean b = true;
		char c = 'A';
		byte e = 5;
		short g = 6;
		int a = 10;
		long l = 20;
		float f = 5.6f;
		double d = 6.6;
		String s = "Techsum";

		System.out.println("Value of boolean=" + b);
		System.out.println("Value of char=" + c);
		System.out.println("Value of byte=" + e);
		System.out.println("Value of short=" + g);
		System.out.println("Value of int=" + a);
		System.out.println("Value of long=" + l);
		System.out.println("Value of float=" + f);
		System.out.println("Value of double=" + d);
		System.out.println("Value of String=" + s);
	}
}
